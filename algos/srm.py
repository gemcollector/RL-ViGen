import hydra
import numpy as np
import torch
import torch.nn as nn
import torch.nn.functional as F

import utils
from utils import random_overlay

from svea import SVEAAgent
from utils import random_mask_freq_v2, random_mask_freq_v1


class SRMAgent(SVEAAgent):


    def update(self, replay_iter, step):
        metrics = dict()

        if step % self.update_every_steps != 0:
            return metrics

        batch = next(replay_iter)
        obs, action, reward, discount, next_obs = utils.to_torch(
            batch, self.device)

        # augment
        obs = self.aug(obs.float())
        original_obs = obs.clone()
        next_obs = self.aug(next_obs.float())

        # strong augmentation + SRM
        aug_obs = self.encoder(random_mask_freq_v2(random_overlay(original_obs)))

        # encode
        obs = self.encoder(obs)
        with torch.no_grad():
            next_obs = self.encoder(next_obs)

        if self.use_tb:
            metrics['batch_reward'] = reward.mean().item()

        # update critic
        metrics.update(
            self.update_critic(obs, action, reward, discount, next_obs, step, aug_obs))

        # update actor
        metrics.update(self.update_actor(obs.detach(), step))

        # update critic target
        utils.soft_update_params(self.critic, self.critic_target,
                                 self.critic_target_tau)

        return metrics